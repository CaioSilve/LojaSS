    private void CboCampoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CboCampoActionPerformed
        sele = (String) CboCampo.getSelectedItem();
        BtnBuscar.setMnemonic(KeyEvent.VK_ENTER);
        BtnBuscar.setEnabled(true);
        TxtCampo.setEnabled(true);
        if (sele.equals("Nome")){
            sele = "nome_func";
        } else if (sele.equals("CPF")){
            sele = "cpf_func";
        } else if (sele.equals("Endereço")){
            sele = "ende_func";
        } else if (sele.equals("Data de Nascimento")){
            sele = "data_nasc_func";
        } else if (sele.equals("Cidade")){
            sele = "cida_func";
        } else if (sele.equals("Cargo")){
            sele = "cargo_func";
        } else if (sele.equals("Data Admissão")){
            sele = "data_admi_func";
        } else if (sele.equals("Email")){
            sele = "emai_func";
        } else if (sele.equals("CEP")){
            sele = "cep_func";
        } 
    }//GEN-LAST:event_CboCampoActionPerformed

