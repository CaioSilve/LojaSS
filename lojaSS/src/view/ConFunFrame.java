    private void TblClieMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TblClieMouseClicked
        int linha = TblClie.getSelectedRow();
        Object cpf = modelo.getValueAt(linha, 1);
        
        
        con_bd.executaSQL("select * from tab_funcionarios where cpf_func = '" + cpf + "'");
        
        try {
            if (con_bd.resultset.first())
            {
                JOptionPane.showMessageDialog(null, "Nome: " + con_bd.resultset.getString("nome_func") + "\n" +
                "CPF: " + con_bd.resultset.getString("cpf_func") + "\n" +
                "Endereço: " + con_bd.resultset.getString("ende_func") + "\n" +
                "Número: " + con_bd.resultset.getString("nume_ende_func") + "\n" +
                "Cidade: " + con_bd.resultset.getString("cida_func") + "\n" +
                "Cargo: " + con_bd.resultset.getString("cargo_func") + "\n" +
                "Data Admissão: " + con_bd.resultset.getString("data_admi_func") + "\n" +
                "Data Nascimento: " + con_bd.resultset.getString("data_nasc_func") + "\n" +
                "CEP: " + con_bd.resultset.getString("cep_func") + "\n" +
                "E-mail: " + con_bd.resultset.getString("emai_func") + "\n");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_TblClieMouseClicked


