    public void preencherTabela(String sql){
        ArrayList dados = new ArrayList();
        
        String[] colunas = new String[]{
            "Nome",
            "CPF",
            "Cidade"
        };
        
        
        con_bd.executaSQL(sql);
        
         try {
             con_bd.resultset.first();
             do{
                 dados.add(new Object[]{con_bd.resultset.getString("nome_func"), con_bd.resultset.getString("cpf_func"), con_bd.resultset.getString("cida_func")});
             }while(con_bd.resultset.next());
         } catch (SQLException ex) {
              JOptionPane.showMessageDialog (null, campo + " não existe no campo " + CboCampo.getSelectedItem(),"Preencher Tabela", 2);
              TxtCampo.setText("");
              BtnBuscar.setMnemonic(0);
              BtnBuscar.setEnabled(false);
              TxtCampo.setEnabled(false);
              preencherTabela("select * from tab_funcionarios order by nome_func");
         }
         
         modelo = new tableModel(dados, colunas);
         
         TblClie.setModel(modelo);
         

         TblClie.getColumnModel().getColumn(0).setPreferredWidth(250);
         TblClie.getColumnModel().getColumn(0).setResizable(true);
         
         TblClie.getColumnModel().getColumn(1).setPreferredWidth(110);
         TblClie.getColumnModel().getColumn(1).setResizable(true);

          TblClie.getColumnModel().getColumn(2).setPreferredWidth(136);
         TblClie.getColumnModel().getColumn(2).setResizable(true);

         
         TblClie.getTableHeader().setReorderingAllowed(false);
         TblClie.setAutoResizeMode(TblClie.AUTO_RESIZE_OFF);
         TblClie.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
         
        
        
    }
    
